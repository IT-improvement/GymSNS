package routine.model;

public class Routine {
	private int routineIndex;
	private int userCode;
	private String day;
	private int exerciesIndex;
	
	public int getRoutineIndex() {
		return routineIndex;
	}
	public void setRoutineIndex(int routineIndex) {
		this.routineIndex = routineIndex;
	}
	public int getUserCode() {
		return userCode;
	}
	public void setUserCode(int userCode) {
		this.userCode = userCode;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public int getExerciesIndex() {
		return exerciesIndex;
	}
	public void setExerciesIndex(int exerciesIndex) {
		this.exerciesIndex = exerciesIndex;
	}
	
	
}
